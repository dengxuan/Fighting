        public static String ToJingcaiLottery(this string lotteryId)
        {
            if (Jingcai.Keys.Contains(lotteryId))
            {
                return Jingcai[lotteryId];
            }
            return lotteryId;
        }

        public static string ToLottery(this int lotteryType)
        {
            switch (lotteryType)
            {
                case 1: return "51";
                case 31: return "33";
                case 40: return "35";
                case 2: return "23529";
                case 5: return "10022";
                case 20201:
                case 20202:
                case 20203:
                case 20204:
                case 20205:
                case 20206:
                    return "42";
                case 20401:
                case 20402:
                case 20403:
                case 20404:
                case 20405:
                    return "43";
                default: throw new ArgumentException("LotteryType Not Support: {0}", lotteryType.ToString());
            }
        }


